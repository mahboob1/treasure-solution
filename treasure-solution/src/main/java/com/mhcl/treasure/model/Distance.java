package com.mhcl.treasure.model;

public class Distance {
	private double travelledNorth;
	private double travelledSouth;
	private double travelledEast;
	private double travelledWest;
	 
	public double getTravelledNorth() {
		return travelledNorth;
	}
	public void setTravelledNorth(double travelledNorth) {
		this.travelledNorth = travelledNorth;
	}
	public void addTravelledNorth(double travelledNorth) {
		this.travelledNorth += travelledNorth;
	}
	public double getTravelledSouth() {
		return travelledSouth;
	}
	public void setTravelledSouth(double travelledSouth) {
		this.travelledSouth = travelledSouth;
	}
	public void addTravelledSouth(double travelledSouth) {
		this.travelledSouth += travelledSouth;
	}
	public double getTravelledEast() {
		return travelledEast;
	}
	public void setTravelledEast(double travelledEast) {
		this.travelledEast = travelledEast;
	}
	public void addTravelledEast(double travelledEast) {
		this.travelledEast += travelledEast;
	}
	public double getTravelledWest() {
		return travelledWest;
	}
	public void setTravelledWest(double travelledWest) {
		this.travelledWest = travelledWest;
	}
	public void addTravelledWest(double travelledWest) {
		this.travelledWest += travelledWest;
	}
	
}
